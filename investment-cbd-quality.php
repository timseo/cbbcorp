                                        <p>The fast-growing CBD industry has attracted a wide variety of operators who bring a mix of passion, skill and expertise to the field in an effort to find a business and manufacturing model that works. The result of the industry’s experimental approach, however, has been CBD products of varying degrees of quality – and consumer confusion.</p>
