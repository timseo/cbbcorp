                                        <p>“A set of good manufacturing practices for processing industrial hemp and manufacturing CBD distillate and isolate products will help stabilize the industry and ensure that the consumer gets the product he or she believes they are buying,” said Sean Groer, CBB’s co-founder. “We’ve built our sole-source, closed-loop supply chain with the belief that increased regulation will come soon.”</p>
                                        <p>Although there are no clearly articulated industry standards, CBB is putting in place a set of high standards that all of the industry can live with, while also delivering a reliable, high quality supply of CBD oil and isolate that is critical to customers.</p>
