                                        <p>In addition to its acreage, CBB’s facilities include a 168,000 square foot extraction facility capable of processing 50,000 pounds of biomass each day. Their 20,000 square foot ISO 3 clean lab space allows CBB to research, test and develop new techniques and applications in the CBD industry.</p>
                                        <p>A single, vertically integrated facility has important advantages:</p>
